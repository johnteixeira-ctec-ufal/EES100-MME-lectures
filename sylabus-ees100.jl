# 1. Introdução

Estas são notas de aula para o curso de Métodos Matemáticos para Engenharia, ministrado para os alunos do Programa de Pós-Garuação em Engenharia Civil da Universidade Federal de Alagoas (PPGEC-UFAL). O objetivo deste curso é discutir e fornecer uma visão geral aprofundada de técnicas matemáticas para a análise de sistemas de engenharia práticos e de interesse aos alunos. Tópicos avançados serão propostos como projetos para os alunos. Além de desenvolver capacidades analíticas essenciais, os alunos ganharão proficiência com várias abordagens computacionais usadas para resolver problemas. As aplicações serão enfatizadas, incluindo mecânica de fluidos, elasticidade/vibrações, e geomecânica.

Para um desenvolvimento **ideal** das lições, trabalhos e projetos propostos ao longo do curso, é interessante o uso do sistema operacional Linux, preferencialmente distribuições baseadas em Debian[^1]. No entanto, para as lições e exemplos destas notas, usamos a linguagem [Julia](https://julialang.org) recentemente desenvolvida,  *opensource* (código aberto) e disponível para qualquer sistema operacional (Linux, OSX e Windows). A linguagem Julia foi desenvolvida explicitamente para cálculo numérico com foco em operações de vetores e matrizes, álgebra linear e execução paralela distribuída, com uma sintaxe semelhante ao [MATLAB](https://www.mathworks.com) porém projetada para alto desempenho, onde os programas são compilados automaticamente para código nativo eficientemente via LLVM. Além disso, é uma linguagem dinamicamente tipada (like a python) e tem bom suporte para uso interativo, podendo ser compilada separadamente. 

Referente aos **projetos**, por se tratar de métodos matemáticos, cada problema tem um certo grau de dificuldade (algebrismo), custo computacional e desafios a serem superados. Portanto, os projetos podem ser feitos por grupos de alunos de acordo com a dificuldade do projeto. É desejável que cada grupo escolha uma linguagem de programação (Julia, C/C++, Python, MATLAB,...) para o desenvolvimento, complementando seus estudos. Os projetos devem ser apresentados como um breve relatório descrevendo o problema, abordagem computacional, código desenvolvido, resultados e conclusões. Preferencialmente, o texto pode ser escrito em LATEX para ser facilmente anexado a essas notas.

A avaliação do curso está projetada da seguinte forma:

**1º exame/prova** (20% do conceito): Prova teórica e clássica, com duração média entre 1.5-3 horas.

**2º exame/prova (cumulativa)** (30% do conceito): Projeto final com prazo de entrega em 4 dias, na apresentação dos projetos propostos, teremos tempo reservado para a explicação e tira dúvidas, somente neste dia.

**Lista de exercícios** (50% do conceito): Abrange exercícios algébricos e numéricos com prazos em média de 1-1,5 semana.

